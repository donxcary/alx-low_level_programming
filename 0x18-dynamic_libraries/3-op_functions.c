#include <stdio.h>
/**
 * add - sum of a and b
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - difference of a and b
 * @a: integer 1
 * @b: integer 2
 *
 * Return: difference of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - product of a and b
 * @a: integer 1
 * @b: integer 2
 *
 * Return: product of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division of a by b
 * @a: integer to be divided
 * @b: divisor
 *
 * Return: result of the division of a by b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of division of a by b
 * @a: integer to be divided
 * @b: divisor
 *
 * Return: remainder of the division of a by b
 */
int mod(int a, int b)
{
	return (a % b);
}
